package hut.data.queryinterchangedata;

import java.util.ArrayList;
import java.util.Map;

public class RestrictionOuputData {

	private Map<String, String> variableMap;
	private boolean hasNewVariable;
	private ArrayList<RowData> rowList;
	
	public ArrayList<RowData> getRowList() {
		return rowList;
	}

	public void setRowList(ArrayList<RowData> rowList) {
		this.rowList = rowList;
	}

	public Map<String, String> getVariableMap() {
		return variableMap;
	}

	public void setVariableMap(Map<String, String> variableMap) {
		this.variableMap = variableMap;
	}

	public boolean isHasNewVariable() {
		return hasNewVariable;
	}

	public void setHasNewVariable(boolean hasNewVariable) {
		this.hasNewVariable = hasNewVariable;
	}
}
