package hut.data.queryinterchangedata;

import java.util.Map;

public class ObjectComboInputData {
	private boolean isNeedNewVariable;
	private Map<String, String> variableMap;
	private String propertyName;
	public Map<String, String> getVariableMap() {
		return variableMap;
	}
	public void setVariableMap(Map<String, String> variableMap) {
		this.variableMap = variableMap;
	}
	public String getPropertyName() {
		return propertyName;
	}
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	
	public ObjectComboInputData(boolean isNeedNewVariable,
			Map<String, String> variableMap, String propertyName) {
		super();
		this.isNeedNewVariable = isNeedNewVariable;
		this.variableMap = variableMap;
		this.propertyName = propertyName;
	}
	public boolean isNeedNewVariable() {
		return isNeedNewVariable;
	}
	public void setNeedNewVariable(boolean isNeedNewVariable) {
		this.isNeedNewVariable = isNeedNewVariable;
	}
}
